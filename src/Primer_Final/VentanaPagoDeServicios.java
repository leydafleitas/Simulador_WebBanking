    private double obtenerMontoMinimo(String servicio){
        // Aquí definimos los montos mínimos para cada servicio.
        switch (servicio) {
            case "ANDE":
                return 50000.0;
            case "ESSAP":
                return 30000.0;
            case "JAHA":
                return 10000.0;
            case "IPS":
                return 20000.0;
            default:
                return 0.0; // Valor por defecto si no se encuentra el servicio
        }
    }
    
