    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // Obtener el monto ingresado desde el campo de texto
        String montoIngresadoTexto = jTextField1.getText();
        double montoIngresado;

        try {
            montoIngresado = Double.parseDouble(montoIngresadoTexto); // Convertir el monto a número
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "El monto ingresado no es válido. Por favor, ingrese un número.");
            return; // Si no es un número válido, detenemos la ejecución
        }

        // Obtener la fecha actual en formato deseado
        SimpleDateFormat formatoFecha = new SimpleDateFormat("dd/MM/yyyy HH:mm:ss");
        String fecha = formatoFecha.format(new Date());

        // Obtener el ID de la cuenta desde el campo de texto
        String cuenta = jTextField2.getText();

        // Validar que el ID de la cuenta no sea vacío
        if (cuenta == null || cuenta.trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor, ingrese un ID de cuenta válido.");
            return; // Detenemos la ejecución si el ID está vacío
        }

        // Obtener el servicio seleccionado desde el combo box
        String servicioSeleccionado = (String) jComboBox1.getSelectedItem();

        // Validar que el servicio no sea nulo o vacío
        if (servicioSeleccionado == null || servicioSeleccionado.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor, seleccione un servicio.");
            return; // Detenemos la ejecución si no hay un servicio seleccionado
        }

        // Crear una instancia del comprobante con los datos recopilados
        ClaseComprobante comprobante = new ClaseComprobante(montoIngresado, fecha, servicioSeleccionado, cuenta);

        // Mostrar el comprobante en un cuadro de diálogo
        JOptionPane.showMessageDialog(this, comprobante.toString(), "Comprobante de Pago", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_jButton2ActionPerformed

