    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // 1. Obtener el servicio seleccionado
        String servicioSeleccionado = (String) jComboBox1.getSelectedItem();

        if (servicioSeleccionado == null || servicioSeleccionado.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor, seleccione un servicio de la lista.");
            return; // Detenemos la ejecución si no hay un servicio seleccionado
        }

        // 2. Obtener el monto ingresado
        String montoIngresadoTexto = jTextField1.getText();
        double montoIngresado;

        // Obtener el número de cédula
        String numCed = jTextField2.getText();

        try {
            montoIngresado = Double.parseDouble(montoIngresadoTexto);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Por favor, ingrese un monto válido.");
            return; // Detenemos la ejecución si el monto no es un número válido
        }

        // 3. Validar que el monto ingresado no sea menor al monto mínimo requerido
        double montoMinimo = obtenerMontoMinimo(servicioSeleccionado);

        if (montoIngresado < montoMinimo) {
            JOptionPane.showMessageDialog(this, "El monto ingresado es menor al mínimo requerido para " + servicioSeleccionado + ": " + montoMinimo);
            return; // Detenemos la ejecución si el monto ingresado es menor al mínimo requerido
        }

        // 4. Seleccionar la cuenta del cliente
        ClaseCuenta cuenta = ClaseArchivos.buscarCuenta(numCed); // Obtener la cuenta desde ClaseArchivos
        if (cuenta == null) {
            JOptionPane.showMessageDialog(this, "No se encontraron cuentas asociadas.");
            return;
        }

        // 5. Mostrar la tarjeta de la cuenta seleccionada
        String tarjetaInfo;
        if (!cuenta.getTarjetasDeCredito().isEmpty()) {
            ClaseTarjetaDeCredito tarjetaCredito = cuenta.getTarjetasDeCredito().get(0);
            tarjetaInfo = "Tarjeta de Crédito: " + tarjetaCredito.getNumeroDeTarjeta() + " con saldo disponible: " + tarjetaCredito.getSaldoDisponible();
            if (tarjetaCredito.getSaldoDisponible() < montoIngresado) {
                JOptionPane.showMessageDialog(this, "Saldo insuficiente en la tarjeta de crédito.");
                return;
            }
        } else if (!cuenta.getTarjetasDeDebito().isEmpty()) {
            ClaseTarjetaDeDebito tarjetaDebito = cuenta.getTarjetasDeDebito().get(0);
            tarjetaInfo = "Tarjeta de Débito: " + tarjetaDebito.getNumeroDeTarjeta() + " con saldo disponible: " + tarjetaDebito.getSaldoCuenta();
            if (tarjetaDebito.getSaldoCuenta() < montoIngresado) {
                JOptionPane.showMessageDialog(this, "Saldo insuficiente en la tarjeta de débito.");
                return;
            }
        } else {
            JOptionPane.showMessageDialog(this, "No se encontraron tarjetas asociadas a la cuenta.");
            return;
        }

        // Mostrar la tarjeta que se usará para el pago
        JOptionPane.showMessageDialog(this, "Se usará la siguiente tarjeta para el pago:\n" + tarjetaInfo);

        // 6. Confirmar el pago y descontar el monto de la tarjeta asociada a la cuenta
        boolean pagoExitoso;
        if (!cuenta.getTarjetasDeCredito().isEmpty()) {
            ClaseTarjetaDeCredito tarjetaCredito = cuenta.getTarjetasDeCredito().get(0);
            tarjetaCredito.setSaldoDisponible(tarjetaCredito.getSaldoDisponible() - montoIngresado);
            ClaseArchivos.actualizarArchivo();
            pagoExitoso = true; // Actualizar el archivo después de hacer el pago
        } else if (!cuenta.getTarjetasDeDebito().isEmpty()) {
            ClaseTarjetaDeDebito tarjetaDebito = cuenta.getTarjetasDeDebito().get(0);
            tarjetaDebito.setSaldoCuenta(tarjetaDebito.getSaldoCuenta() - montoIngresado);
            ClaseArchivos.actualizarArchivo(); // Actualizar el archivo después de hacer el pago
            pagoExitoso = true;
        } else {
            pagoExitoso = false;
        }

        // 7. Mostrar el resultado del pago
        if (pagoExitoso) {
            JOptionPane.showMessageDialog(this, "Pago de " + montoIngresado + " realizado exitosamente desde la cuenta ID: " + cuenta.getID());
        } else {
            JOptionPane.showMessageDialog(this, "Hubo un problema al realizar el pago. Por favor, inténtelo de nuevo.");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

